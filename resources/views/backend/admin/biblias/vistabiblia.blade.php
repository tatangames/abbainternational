    <script>

        function recargar(){
            var ruta = "{{ URL::to('/admin/biblia/tabla') }}";
            $('#tablaDatatable').load(ruta);
        }


        function modalNuevo(){
            document.getElementById("formulario-nuevo").reset();
            $('#modalAgregar').modal('show');
        }

        function nuevo(){

            var titulo = document.getElementById('titulo-nuevo').value;

            if(titulo === '') {
                toastr.error('Título es requerido');
                return;
            }

            openLoading();

            var formData = new FormData();
            formData.append('titulo', titulo);

            axios.post('/admin/biblia/registrar', formData, {
            })
                .then((response) => {
                    closeLoading();
                    if (response.data.success === 1) {
                        $('#modalAgregar').modal('hide');
                        toastr.success('Registrado correctamente');
                        recargar();
                    }
                    else {
                        toastr.error('Error al guardar');
                    }
                })
                .catch((error) => {
                    closeLoading();
                    toastr.error('Error al guardar');
                });
        }


        function informacionBiblia(id){

            openLoading();
            document.getElementById("formulario-editar").reset();

            axios.post('/admin/biblia/informacion',{
                'id': id
            })
                .then((response) => {
                    closeLoading();
                    if(response.data.success === 1){
                        $('#modalEditar').modal('show');
                        $('#id-editar').val(id);
                        $('#titulo-editar').val(response.data.titulo);

                    }else{
                        toastr.error('Información no encontrada');
                    }
                })
                .catch((error) => {
                    closeLoading();
                    toastr.error('Información no encontrada');
                });
        }

        function editar(){

            var id = document.getElementById('id-editar').value;
            var titulo = document.getElementById('titulo-editar').value;

            if(titulo === '') {
                toastr.error('Título es requerido');
                return;
            }

            openLoading();

            var formData = new FormData();
            formData.append('titulo', titulo);
            formData.append('idbiblia', id);

            axios.post('/admin/biblia/actualizar', formData, {
            })
                .then((response) => {
                    closeLoading();
                    if (response.data.success === 1) {
                        $('#modalEditar').modal('hide');
                        toastr.success('Actualizado correctamente');
                        recargar();
                    }
                    else {
                        toastr.error('Error al guardar');
                    }
                })
                .catch((error) => {
                    closeLoading();
                    toastr.error('Error al guardar');
                });
        }



        function preguntaActivar(idbiblia){

            Swal.fire({
                title: '¿Activar?',
                text: "",
                icon: 'question',
                showCancelButton: true,
                confirmButtonColor: '#28a745',
                cancelButtonColor: '#d33',
                cancelButtonText: 'Cancelar',
                confirmButtonText: 'Si'
            }).then((result) => {
                if (result.isConfirmed) {
                    configurar(1, idbiblia);
                }
            })
        }


        function preguntaDeshabilitar(idbiblia){

            Swal.fire({
                title: '¿Deshabilitar?',
                text: "",
                icon: 'question',
                showCancelButton: true,
                confirmButtonColor: '#28a745',
                cancelButtonColor: '#d33',
                cancelButtonText: 'Cancelar',
                confirmButtonText: 'Si'
            }).then((result) => {
                if (result.isConfirmed) {
                    configurar(0, idbiblia);
                }
            })
        }


        function configurar(estado, idbiblia){

            let formData = new FormData();
            formData.append('idbiblia', idbiblia);
            formData.append('estado', estado);
            openLoading();

            axios.post('/admin/biblia/activacion', formData, {
            })
                .then((response) => {
                    closeLoading();

                    if(response.data.success === 1){
                        toastr.success('Actualizado');
                        recargar();
                    }
                    else{
                        toastr.error('Error al actualizar');
                    }
                })
                .catch((error) => {
                    toastr.error('Error al actualizar');
                    closeLoading();
                });

        }


        function vistaLibros(idbiblia){
            window.location.href="{{ url('/admin/biblialibro/vista') }}/" + idbiblia;
        }


    </script>


